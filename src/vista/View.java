    private void jBotonSeleccionarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBotonSeleccionarMouseClicked
        if (jComboEquipo.getSelectedIndex()==0){
            jBotonComprar.setEnabled(true);
            jComboTamanoTV.setEnabled(false);
            jComboCapacidadipod.setEnabled(false);
            jTextCantidadCDs.setEnabled(true);
        } else if (jComboEquipo.getSelectedIndex()==1){
            jBotonComprar.setEnabled(true);
            jComboTamanoTV.setEnabled(false);
            jComboCapacidadipod.setEnabled(true);
            jTextCantidadCDs.setEnabled(false);
        } else if (jComboEquipo.getSelectedIndex()==2){
            jBotonComprar.setEnabled(true);
            jComboTamanoTV.setEnabled(true);
            jComboCapacidadipod.setEnabled(false);
            jTextCantidadCDs.setEnabled(false);
        }
        
    }//GEN-LAST:event_jBotonSeleccionarMouseClicked

